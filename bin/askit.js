#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const [, , command, targetArg, ...rest] = process.argv;

function log(message) {
  console.log(message);
}

function fail(message) {
  console.error(`✖ ${message}`);
  process.exit(1);
}

function exists(p) {
  return fs.existsSync(p);
}

function readFileSafe(p) {
  return fs.readFileSync(p, 'utf8');
}

function mkdirp(p) {
  fs.mkdirSync(p, { recursive: true });
}

function render(template, vars) {
  return template.replace(/\{\{(\w+)\}\}/g, (_, key) => vars[key] ?? '');
}

function initSkill(name) {
  if (!name) fail('Please provide a skill folder name. Example: askit init my-skill');
  const root = path.resolve(process.cwd(), name);
  if (exists(root)) fail(`Target already exists: ${root}`);

  mkdirp(root);
  mkdirp(path.join(root, 'scripts'));
  mkdirp(path.join(root, 'references'));
  mkdirp(path.join(root, 'assets'));

  const templatePath = path.resolve(process.cwd(), 'templates/skill/SKILL.md.tpl');
  const fallbackTemplate = `---\nname: {{name}}\ndescription: A short description of what this skill does and when to use it.\n---\n\n# {{title}}\n\n## When to Use\n\nDescribe when this skill should be triggered.\n\n## Prerequisites\n\nList required tools or accounts.\n\n## Usage\n\n### Basic Usage\n\nExplain the workflow.\n\n## Examples\n\n### Example 1\n\nAdd a concrete example.\n`;
  const template = exists(templatePath) ? readFileSafe(templatePath) : fallbackTemplate;
  const title = name
    .split('-')
    .map(part => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');

  const skillMd = render(template, {
    name,
    title,
    description: `Describe what ${name} does and when to use it.`
  });

  fs.writeFileSync(path.join(root, 'SKILL.md'), skillMd);
  fs.writeFileSync(path.join(root, 'README.md'), `# ${name}\n\nGenerated by agent-skill-kit.\n`);

  log(`✔ Created skill scaffold at ${root}`);
  log('Next: edit SKILL.md, then run askit lint <folder>');
}

function lintSkill(target = '.') {
  const root = path.resolve(process.cwd(), target);
  const skillPath = path.join(root, 'SKILL.md');
  const issues = [];

  if (!exists(root)) fail(`Path not found: ${root}`);

  if (!exists(skillPath)) {
    issues.push('Missing SKILL.md');
  } else {
    log('✔ Found SKILL.md');
    const content = readFileSafe(skillPath);

    if (/description:\s*.+/i.test(content)) {
      log('✔ Description exists');
    } else {
      issues.push('Missing frontmatter description');
    }

    if (/##\s+When to Use/i.test(content)) {
      log('✔ Contains When to Use section');
    } else {
      issues.push('Missing "When to Use" section');
    }

    if (/##\s+Usage/i.test(content)) {
      log('✔ Contains Usage section');
    } else {
      issues.push('Missing "Usage" section');
    }

    if (/##\s+Examples/i.test(content)) {
      log('✔ Contains Examples section');
    } else {
      issues.push('Missing "Examples" section');
    }
  }

  if (issues.length === 0) {
    log('✔ Lint passed');
    return;
  }

  for (const issue of issues) {
    console.log(`⚠ ${issue}`);
  }
  process.exitCode = 1;
}

function checklist() {
  const items = [
    'SKILL.md has clear name and description',
    'Usage flow is concrete, not hand-wavy',
    'At least one real example exists',
    'Prerequisites are documented',
    'Any helper scripts are included',
    'README explains value in plain language',
    'License is present',
    'Repo is ready for public readers'
  ];

  log('Release checklist:');
  for (const item of items) {
    log(`- ${item}`);
  }
}

switch (command) {
  case 'init':
    initSkill(targetArg);
    break;
  case 'lint':
    lintSkill(targetArg);
    break;
  case 'checklist':
    checklist();
    break;
  default:
    log('agent-skill-kit');
    log('Usage:');
    log('  askit init <name>');
    log('  askit lint [path]');
    log('  askit checklist');
}
